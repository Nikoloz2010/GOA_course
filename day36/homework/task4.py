# მომხმარებლისგან წინადადების შეყვანა
sentence = input("შეიყვანეთ წინადადება: ")

# დაბეჭდავს წინადადებას პატარა ასოებით
print("Lower:", sentence.lower())

# დაბეჭდავს წინადადებას დიდი ასოებით
print("Upper:", sentence.upper())

# დაბეჭდავს წინადადებას მხოლოდ პირველი ასოთი დიდად
print("Capitalize:", sentence.capitalize())